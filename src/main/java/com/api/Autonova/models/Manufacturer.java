package com.api.Autonova.models;

public class Manufacturer {

    private String manufacturerId;
    private String manufacturerBrandId;
    private String manufacturerName;

    public String getManufacturerId() {
        return manufacturerId;
    }

    public void setManufacturerId(String manufacturerId) {
        this.manufacturerId = manufacturerId;
    }

    public String getManufacturerBrandId() {
        return manufacturerBrandId;
    }

    public void setManufacturerBrandId(String manufacturerBrandId) {
        this.manufacturerBrandId = manufacturerBrandId;
    }

    public String getManufacturerName() {
        return manufacturerName;
    }

    public void setManufacturerName(String manufacturerName) {
        this.manufacturerName = manufacturerName;
    }
}
